package C20160625_FlowControl.Conditionals;

public class ExTernaryOperators {
    public static void main(String[] args) {

        int age = 17;
        //Ternary Operator -> concise way to write an if-else statement on a single line.
        String state = (age >= 18) ? "Age is greater than" : "Age is not greater than";
        System.out.println(state);



    }
}
